package com.valdal14.encapsulation;

public class Customer {

    private String firstName;
    private String lastName;
    private String credicCardNumber;

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getCredicCardNumber() {
        return credicCardNumber;
    }

    public void setCredicCardNumber(String credicCardNumber) {
        this.credicCardNumber = credicCardNumber;
    }

}
