package com.valdal14.multithreading.synchblocks;

public class Person {

    private String name;
    private String last;

    public Person(String name, String last) {
        this.name = name;
        this.last = last;
    }

    public String getName() {
        return name;
    }

    public String getLast() {
        return last;
    }
}
